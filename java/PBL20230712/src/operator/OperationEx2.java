package operator;

public class OperationEx2 {

	public static void main(String[] args) {
		int i = 1;
		System.out.println(i);
		i = i + 1;
		System.out.println(i);
		i = i + 1;
		System.out.println(i);
		
		++i; // i = i + 1 
		System.out.println(i);
		++i; // i = i + 1
		System.out.println(i);
		// ++ : 1증가연산자
		i = i + 1;
		System.out.println(i);
		++i;
		System.out.println(i);
		i++;
		System.out.println(i);
		i++;
		System.out.println(i);
	}

}
