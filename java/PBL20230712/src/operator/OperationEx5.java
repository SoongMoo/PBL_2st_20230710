package operator;

public class OperationEx5 {
	public static void main(String[] args) {
		// 비교(관계)연산자 : < , > , <=, >= ,  !=, ==
		// 결과값은 참 / 거짓 : boolean
		int i = 10;
		System.out.println(i > 3);
		System.out.println(i < 3);
		System.out.println(i <= 10);
		System.out.println(i >= 3);
		System.out.println(i == 3);
		System.out.println(i != 3);
		
		
	}
}
